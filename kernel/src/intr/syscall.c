#include <intr/syscall.h>
#include <libkern/log.h>
#include <stdint.h>

#define MAX_SYSCALLS 1


struct SycallRegs 
{
    uint64_t rbx;
    uint64_t rcx;
    uint64_t rdx;
    uint64_t rsi;
    uint64_t rdi;
    uint64_t r8;
    uint64_t r9;
    uint64_t r10;
} __attribute__((packed)) syscall_regs;


static void sys_hello(void)
{
    kprintf(KINFO "<SYS_HELLO>: Hello from userspace!\n");
}


void(*syscall_table)() = {
    sys_hello
};
